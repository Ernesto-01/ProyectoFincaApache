    public void llenarTabla() {
        int fila=this.tblJornalero.getSelectedRow();
        double ns=0.0;
        try {
            ns=cjo.mostrarSalarioDiario((int) this.tblJornalero.getValueAt(fila, 7));
        } catch (Exception e) {
        }
        this.txtNombre.setText(String.valueOf(this.tblJornalero.getValueAt(fila, 1)));
        this.txtApellido.setText(String.valueOf(this.tblJornalero.getValueAt(fila, 2)));
        this.txtEdad.setText(String.valueOf(this.tblJornalero.getValueAt(fila, 3)));
        this.txtHorario.setText(String.valueOf(this.tblJornalero.getValueAt(fila, 4)));
        this.txtEstado.setText(String.valueOf(this.tblJornalero.getValueAt(fila, 5)));
        this.txtDireccion.setText(String.valueOf(this.tblJornalero.getValueAt(fila, 6)));
        this.cbPagos.setSelectedItem(ns);

    }
    
    public void tablaJornaleros(String buscar) {
        
        String [] columnas={"Codigo Jornalero ","Nombre","Apellido","Edad"," Horario","Estado","Direccion","Pago"};
        Object [] obj=new Object[8];
        DefaultTableModel tabla=new DefaultTableModel(null,columnas);
        List ls;
        try {
            ls=cjo.mostrarJornaleros(buscar);
            for(int i=0;i<ls.size();i++){
                jo=(Jornaleros) ls.get(i);
                obj[0]=jo.getIdJornalero();
                obj[1]=jo. getNombre();
                obj[2]=jo.getApellido();
                obj[3]=jo.getEdad();
                obj[4]=jo.getHorario();
                obj[5]=jo.getEstado();
                obj[6]=jo.getDireccion();
                obj[7]=jo.getIdPagos();
               
                tabla.addRow(obj);
            }
            ls=cjo.mostrarJornaleros(buscar);
            this.tblJornalero.setModel(tabla);
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, "Erro : "+e.toString());
        }
    }
    
