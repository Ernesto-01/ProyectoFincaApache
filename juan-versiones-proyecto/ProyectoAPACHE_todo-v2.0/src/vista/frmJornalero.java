    public void mantenimiento(int accion) {
        int lsi;
       
        try {
            String p="";
            String t="";
            String s="";
            if (validacion()) {
                if (accion == 1 || accion == 2) {    
                    jo.setNombre(this.txtNombre.getText());
                    jo.setApellido(this.txtApellido.getText());
                    jo.setEdad(Integer.parseInt(this.txtEdad.getText()));
                    jo.setHorario(this.txtHorario.getText());
                    jo.setEstado(Integer.parseInt(this.txtEstado.getText()));
                    jo.setDireccion(this.txtDireccion.getText());
                    lsi=cjo.mostrarIDPagos(Double.parseDouble(this.cbPagos.getSelectedItem().toString()));
                    jo.setIdPagos(lsi);
                    
                    //JOptionPane.showMessageDialog(this, lsi);
                    if (accion == 1) {                                           
                        jo.setIdJornalero(ICONIFIED);
                        p="¿Desea insertar el registro?";
                        t="Insertar";
                        s="Insertado con exito";
                    } else if(accion == 2) {
                        int fila=this.tblJornalero.getSelectedRow();
                        jo.setIdJornalero(Integer.parseInt(String.valueOf(this.tblJornalero.getValueAt(fila, 0))));
                        p="¿Desea modificar el registro?";
                        t="Modificar";
                        s="Modificado con exito";
                    }
                } else if (accion == 3) {
                    int fila=this.tblJornalero.getSelectedRow();
                    jo.setIdJornalero(Integer.parseInt(String.valueOf(this.tblJornalero.getValueAt(fila, 0))));
                    jo.setNombre("");
                    jo.setApellido("");
                    jo.setEdad(0);
                    jo.setHorario("");
                    jo.setEstado(0);
                    jo.setDireccion("");
                    jo.setIdPagos(0);
                    
                    p="¿Desea eliminar el registro?";
                    t="Eliminar";
                    s="Eliminado con exito";
                }
                int sino = JOptionPane.showConfirmDialog(this, p,t,JOptionPane.YES_NO_OPTION);
                if(sino==0){
                    cjo.mantenimientoJornaleros(accion,jo);
                    JOptionPane.showMessageDialog(rootPane, s,"Confirmacion",JOptionPane.INFORMATION_MESSAGE);
                    tablaJornaleros("");// m
                    limpiar();
                } else {
                    limpiar();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Error: Debe revisar datos a ingresar.", "ERROR!", JOptionPane.ERROR_MESSAGE);
            }
        } catch(Exception e) {
            e.printStackTrace();
        }
    }
    
    public boolean validacion() {
        boolean resp=false;
        if (!"".equals(this.txtNombre.getText()) || !"".equals(this.txtApellido.getText())|| !"".equals(this.txtEdad.getText())|| !"".equals(this.txtHorario.getText())|| !"".equals(this.txtEstado.getText())||!"".equals(this.txtDireccion.getText())|| this.cbPagos.getSelectedIndex()!=0) {
            resp=true;
        }
        return resp;
    }

    
    
