    public void llenarTabla() {
        int fila=this.tblTareaDiaria.getSelectedRow();
        String ni="";
        String nt="";
        String nj="";
        try {
            ni=ctd.mostrarNombreTarea((int) this.tblTareaDiaria.getValueAt(fila, 5));
            nt=ctd.mostrarNombreTablon((int) this.tblTareaDiaria.getValueAt(fila, 6));
            nj=ctd.mostrarNombreJornalero((int) this.tblTareaDiaria.getValueAt(fila, 7));
        } catch (Exception e) {
        }
        this.txtFecha.setText(String.valueOf(this.tblTareaDiaria.getValueAt(fila, 1)));
        this.txtEstado.setText(String.valueOf(this.tblTareaDiaria.getValueAt(fila, 2)));
        this.txtJornada.setText(String.valueOf(this.tblTareaDiaria.getValueAt(fila, 3)));
        this.txtPorcentaje.setText(String.valueOf(this.tblTareaDiaria.getValueAt(fila, 4)));
        this.cbTarea.setSelectedItem(ni);
        this.cbTablon.setSelectedItem(nt);
          this.cbJornalero.setSelectedItem(nj);
    }
    
    public void tablaTareaDiaria(int buscar) {
        
        String [] columnas={"Codigo Tarea Diaria","Fecha","Estado","Jornada","Porcentaje" ,"Tarea","Tablon","Jornalero"};
        Object [] obj=new Object[8];
        DefaultTableModel tabla=new DefaultTableModel(null,columnas);
        List ls;
        try {
            ls=ctd.mostrarTareaDiaria(buscar);
            for(int i=0;i<ls.size();i++){
                td=(TareaDiaria) ls.get(i);
                obj[0]=td.getIdTareaDiaria();
                obj[1]=td.getFecha();
                obj[2]=td.getEstado();
                obj[3]=td.getJornada();
                 obj[4]=td.getPorcentajeRealizado();
                obj[5]=td.getIdTarea();
                obj[6]=td.getIdTablones();
                obj[7]=td.getIdJornalero();
                tabla.addRow(obj);
            }
            ls=ctd.mostrarTareaDiaria(buscar);
            this.tblTareaDiaria.setModel(tabla);
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, "Erro : "+e.toString());
        }
    }
    
