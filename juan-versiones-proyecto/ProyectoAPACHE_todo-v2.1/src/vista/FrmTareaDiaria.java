    public void mantenimiento(int accion) {
        int lsi;
        int lst;
        int lsj;
        try {
            String p="";
            String t="";
            String s="";
            if (validacion()) {
                if (accion == 1 || accion == 2) {    
                    td.setFecha(this.txtFecha.getText());
                    lsi=ctd.mostrarIDTarea(this.cbTarea.getSelectedItem().toString());
                    td.setIdTarea(lsi);
                    lst=ctd.mostrarIDTablon(this.cbTablon.getSelectedItem().toString());
                    td.setIdTablones(lst);
                    lsj=ctd.mostrarIDJornalero(this.cbJornalero.getSelectedItem().toString());
                    td.setIdJornalero(lst);
                    //JOptionPane.showMessageDialog(this, lsi+" "+lst);
                    if (accion == 1) {                                           
                        td.setIdTareaDiaria(ICONIFIED);
                        p="¿Desea insertar el registro?";
                        t="Insertar";
                        s="Insertado con exito";
                    } else if(accion == 2) {
                        int fila=this.tblTareaDiaria.getSelectedRow();
                        td.setIdTareaDiaria(Integer.parseInt(String.valueOf(this.tblTareaDiaria.getValueAt(fila, 0))));
                        p="¿Desea modificar el registro?";
                        t="Modificar";
                        s="Modificado con exito";
                    }
                } else if (accion == 3) {
                    int fila=this.tblTareaDiaria.getSelectedRow();
                    td.setIdTareaDiaria(Integer.parseInt(String.valueOf(this.tblTareaDiaria.getValueAt(fila, 0))));
                    td.setFecha("");
                    td.setEstado(0);
                    td.setJornada("");
                       td.setPorcentajeRealizado("");
                    td.setIdTarea(0);
                    
                    td.setIdTablones(0);
                    td.setIdJornalero(0);
                    
                    p="¿Desea eliminar el registro?";
                    t="Eliminar";
                    s="Eliminado con exito";
                }
                int sino = JOptionPane.showConfirmDialog(this, p,t,JOptionPane.YES_NO_OPTION);
                if(sino==0){
                    ctd.mantenimientoTareaDiaria(accion,td);
                    JOptionPane.showMessageDialog(rootPane, s,"Confirmacion",JOptionPane.INFORMATION_MESSAGE);
                    tablaTareaDiaria(0);
                    limpiar();
                } else {
                    limpiar();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Error: Debe revisar datos a ingresar.", "ERROR!", JOptionPane.ERROR_MESSAGE);
            }
        } catch(Exception e) {
            e.printStackTrace();
        }
    }
    
    public boolean validacion() {
        boolean resp=false;
        if (!"".equals(this.txtFecha.getText()) || !"".equals(this.txtEstado.getText()) ||!"".equals(this.txtJornada.getText()) || !"".equals(this.txtPorcentaje.getText()) ||this.cbTarea.getSelectedIndex()!=0 || this.cbTablon.getSelectedIndex()!=0 || this.cbJornalero.getSelectedIndex()!=0) {
            resp=true;
        }
        return resp;
    }

