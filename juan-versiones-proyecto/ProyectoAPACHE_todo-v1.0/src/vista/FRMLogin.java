    public void verificar() {

        Object[] obj = new Object[4];
        DaoLogin cc = new DaoLogin();
        List ls;
        Login lo = new Login();
        lo.setNickname(jTextField1.getText());
        lo.setPasswrd(String.valueOf(jPasswordField1.getPassword()));
        String msjVeri = "";
        try {
            ls = cc.verificar(lo);
            for (int i = 0; i < ls.size(); i++) {
                lo = (Login) ls.get(i);          
                obj[0] = lo.getNickname();
                obj[1] = lo.getPasswrd();        
                obj[2] = lo.getIdRol();             
            }
            ls = cc.verificar(lo);
            int tipo = (int) obj[2];
            if(tipo == 1)
            {
                msjVeri = "Bienvenido Administrador";
            }
            if(tipo == 2)
            {
                msjVeri = "Bienvenido Secretaria";
            }
            if(tipo == 3)
            {
                msjVeri = "Bienvenido Caporal";
            }
JOptionPane.showMessageDialog(this,msjVeri);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Usuario o contraseÃ±a incorrecta","Informacion",JOptionPane.ERROR_MESSAGE );
        }

    }

