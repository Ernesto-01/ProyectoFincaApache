    public void llenarTabla() {
        int fila = this.tblTareas.getSelectedRow();
        this.txtNombre.setText(String.valueOf(this.tblTareas.getValueAt(fila, 1)));
        this.txtTipoTarea.setText(String.valueOf(this.tblTareas.getValueAt(fila, 2)));
    }

    public void tablaTarea(String buscar) {

        String[] columnas = {"Codigo Tarea", "Nombre Tarea", "Tipo de Tarea"};
        Object[] obj = new Object[3];
        DefaultTableModel tabla = new DefaultTableModel(null, columnas);
        List ls;
        try {
            ls = ct.mostrarTareas(buscar);
            for (int i = 0; i < ls.size(); i++) {
                ta = (Tareas) ls.get(i);
                obj[0] = ta.getIdTarea();
                obj[1] = ta.getNombre();
                obj[2] = ta.getTipoTarea();
                tabla.addRow(obj);
            }
            ls = ct.mostrarTareas(buscar);
            this.tblTareas.setModel(tabla);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error : " + e.toString());
        }
    }

    public void mantenimiento(int accion) {
        try {
            String p = "";
            String t = "";
            String s = "";
            if (validacion()) {
                if (accion == 1 || accion == 2) {
                    ta.setNombre(this.txtNombre.getText());
                    ta.setTipoTarea(this.txtTipoTarea.getText());
                    if (accion == 1) {
                        ta.setIdTarea(ICONIFIED);
                        p = "¿Desea insertar el registro?";
                        t = "Insertar";
                        s = "Insertado con exito";
                    } else if (accion == 2) {
                        int fila = this.tblTareas.getSelectedRow();
                        ta.setIdTarea(Integer.parseInt(String.valueOf(this.tblTareas.getValueAt(fila, 0))));
                        p = "¿Desea modificar el registro?";
                        t = "Modificar";
                        s = "Modificado con exito";
                    }
                } else if (accion == 3) {
                    int fila = this.tblTareas.getSelectedRow();
                    ta.setIdTarea(Integer.parseInt(String.valueOf(this.tblTareas.getValueAt(fila, 0))));
                    ta.setNombre("");
                    ta.setTipoTarea("");
                    p = "¿Desea eliminar el registro?";
                    t = "Eliminar";
                    s = "Eliminado con exito";
                }
                int sino = JOptionPane.showConfirmDialog(this, p, t, JOptionPane.YES_NO_OPTION);
                if (sino == 0) {
                    ct.mantenimientoTareas(accion, ta);
                    JOptionPane.showMessageDialog(rootPane, s, "Confirmacion", JOptionPane.INFORMATION_MESSAGE);
                    tablaTarea("");

                    utilidades.LimpiarCampos(jPanel1);
                } else {

                    utilidades.LimpiarCampos(jPanel1);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public boolean validacion() {
        boolean resp = false;
        if (!"".equals(this.txtNombre.getText()) || !"".equals(this.txtTipoTarea.getText())) {
            resp = true;
        }
        return resp;
    }

