public void ocultarPanel(JComponent comp){
    int posicion = comp.getX();
    if(posicion > -1){
        this.menu1.setVisible(true);
        animacion.mover_izquierda(0, -264, 3, 2, comp);

        if (comp.getX()==0) {
            System.out.println("Menu Oculto");

            this.menu2p.setVisible(true);

        }

    }else{
        if (comp.getX()!=0) {
            comp.setVisible(true);

            animacion.mover_derecha(-264, 0, 3, 2, comp);
            System.out.println("Menu visble");
            this.menu2p.setVisible(false);

        }
    }
        
}
