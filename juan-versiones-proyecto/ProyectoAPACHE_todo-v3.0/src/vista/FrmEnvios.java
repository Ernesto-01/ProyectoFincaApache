    public void llenarTabla() {
        int fila=this.tblEnvios.getSelectedRow();
        String nf = "";
        //String ni = "";
        try {
            nf = ce.mostrarNombreFinca((int) this.tblEnvios.getValueAt(fila, 4));
            //ni = ce.mostrarNombreInsumos((int) this.tblEnvios.getValueAt(fila, 5));
        } catch (Exception e) {
        }
        this.txtNombreEnvio.setText(String.valueOf(this.tblEnvios.getValueAt(fila, 1)));
        //this.dcFechaEnvio.setSelectedDate(String.valueOf(this.tblEnvios.getValueAt(fila, 2)));
        this.txtNombreReceptor.setText(String.valueOf(this.tblEnvios.getValueAt(fila, 3)));
        this.cbxFinca.setSelectedItem(nf);
        //this.cbxInsumo.setSelectedItem(ni);
    }
    
