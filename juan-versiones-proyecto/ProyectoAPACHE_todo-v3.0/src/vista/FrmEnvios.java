    public void mantenimiento(int accion) {
        int lsf;
        int lsi;
        try {
            String p="";
            String t="";
            String s="";
            if (validacion()) {
                if (accion==1 || accion ==2) {                           
                    env.setIdEnvios(ICONIFIED);
                    env.setFechaEnvio(this.dcFechaEnvio.getText());
                    env.setCantidadEnvios(Integer.parseInt(this.txtCantidad.getText()));
                    env.setNombreReceptor(this.txtNombreReceptor.getText());
                    lsf = ce.mostrarIDFinca(this.cbxFinca.getSelectedItem().toString());
                    env.setIdFinca(lsf);
                    lsi = ce.mostrarIDInsumo(this.cbxInsumo.getSelectedItem().toString());
                    env.setIdInsumo(lsi);
                    if (accion == 1) {
                        p="¿Desea insertar el registro?";
                        t="Insertar";
                        s="Insertado con exito";
                    } else if(accion==2) {                    
                        int fila=this.tblEnvios.getSelectedRow();
                       env.setIdEnvios(Integer.parseInt(String.valueOf(this.tblEnvios.getValueAt    (fila, 0))));
                        p="¿Desea modificar el registro?";
                        t="Modificar";
                        s="Modificado con exito";
                    }
                } else if (accion==3) {
                    env.setFechaEnvio("");
                    env.setCantidadEnvios(0);
                    env.setNombreReceptor("");
                    env.setIdFinca(0);
                    env.setIdInsumo(0);
                    p="¿Desea eliminar el registro?";
                    t="Eliminar";
                    s="Eliminado con exito";
                }
                int sino = JOptionPane.showConfirmDialog(this, p,t,JOptionPane.YES_NO_OPTION);
                if(sino==0){
                    ce.mantenimientoEnvio(accion,env);
                    JOptionPane.showMessageDialog(rootPane, s,"Confirmacion",JOptionPane.INFORMATION_MESSAGE);
                    tablaE("");
                    limpiar();
                } else {
                    limpiar();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Error: Debe revisar datos a ingresar.", "ERROR!", JOptionPane.ERROR_MESSAGE);
            }
        } catch(Exception e) {
            e.printStackTrace();
        }
    }
    
    public boolean validacion() {
        boolean resp=false;
        if (!"".equals(this.txtNombreEnvio.getText()) && !"".equals(this.txtCantidad.getText()) && this.cbxFinca.getSelectedIndex()!=0 && this.cbxInsumo.getSelectedIndex()!=0 && !"".equals(this.dcFechaEnvio.getSelectedDate())) {
            resp=true;
        }
        return resp;
    }

